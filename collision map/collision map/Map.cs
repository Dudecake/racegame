        public void setMap(Objects obj)
        {
            int width = Maps[mapCounter].GetLength(1);
            int height = Maps[mapCounter].GetLength(0);
            for (int x = 0; x < width; x++)
            {
                for (int y = 0; y < height; y++)
                {
                    int numb = Maps[mapCounter][y, x];
                    if (numb != 0)
                    {
                        switch (numb)
                        {
                            case 1:
                                obj.DrawRoad(tileSqr, tileSqr, x * tileSqr, y * tileSqr);
                                //iManager.inGameSprites.Add(new Sprite(Properties.Resources.brick, x * tileSqr, y * tileSqr, tileSqr, tileSqr, Sprite.SpriteType.Floor));
                                break;
                            case 2:
                                obj.DrawGrass(tileSqr, tileSqr, x * tileSqr, y * tileSqr);
                                //iManager.inGameSprites.Add(new Sprite(Properties.Resources.Dementia_GTA2, x * tileSqr, y * tileSqr, tileSqr, tileSqr, Sprite.SpriteType.Floor));
                                break;
                            case 3:
                                obj.DrawWall(tileSqr, tileSqr, x * tileSqr, y * tileSqr);
                                break;
                            case 4:
                                obj.DrawCheck(tileSqr, tileSqr, x * tileSqr, y * tileSqr);
                                break;
                            case 5:
                                obj.DrawStart(tileSqr, tileSqr, x * tileSqr, y * tileSqr);
                                break;
                            case 6:
                                obj.DrawPitStop(tileSqr, tileSqr, x * tileSqr, y * tileSqr);
                                break;
                        }
                    }
                }
            }
        }
